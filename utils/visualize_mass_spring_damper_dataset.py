import numpy as np
import matplotlib.pyplot as plt
x_train = np.load('experiments/datasets/mass_spring_damper_x_train.npy')
x_train = x_train.reshape(-1, 2)
c = np.arange(len(x_train))
np.random.shuffle(c)
x_train = x_train[c[::int(100/1)]]
plt.scatter(x_train[:, 0], x_train[:, 1], alpha=0.01)
plt.show()
plt.savefig('mass_spring_damper_train.png')
plt.close()
x_val = np.load('experiments/datasets/mass_spring_damper_x_val.npy').reshape(-1, 2)
plt.scatter(x_val[:, 0], x_val[:, 1])
plt.savefig('mass_spring_damper_val.png')
plt.close()
figure = plt.figure(frameon=False)
figure.scatter(x_train[:, 0], x_train[:, 1], s=4, alpha=0.8, label='Training')
figure.scatter(x_val[:, 0], x_val[:, 1], s=4, label='Testing')
# plt.xlim([-4, 6.3])
# plt.ylim([-6.3, 6.3])
figure.xlabel('x')
figure.ylabel('x_dt')
figure.legend(loc="upper left")
plt.gca().set_aspect('equal')
figure.savefig('mass_spring_damper_trainval.png')
figure.close()
