import numpy as np
x_train = np.load('experiments/datasets/mass_spring_damper_x_train.npy')
import matplotlib.pyplot as plt
c = np.arange(len(x_train))
np.random.shuffle(c)
x_train = x_train[c[::int(100/1)]]
plt.scatter(x_train[:,0], x_train[:,1], alpha=0.01)
plt.show()
plt.savefig('mass_spring_damper_train.png')
plt.close()
x_val = np.load('experiments/datasets/mass_spring_damper_x_val.npy')
plt.scatter(x_val[:,0], x_val[:,1])
plt.savefig('mass_spring_damper_val.png')
plt.close()
plt.scatter(x_train[:,0], x_train[:,1], s=4, alpha=0.8, label='Training')
plt.scatter(x_val[:,0], x_val[:,1], s=4, label='Testing')
# plt.xlim([-4, 6.3])
# plt.ylim([-6.3, 6.3])
plt.xlabel('x')
plt.ylabel('x_dt')
plt.legend(loc="upper left")
plt.savefig('mass_spring_damper_trainval.png')
plt.close()
