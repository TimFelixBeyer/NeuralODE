plt.close()

x_val = np.load('experiments/datasets/airplane_lat_long_x_val.npy')
x_val = np.reshape(x_val, (-1, 8))

plt.scatter(x_val[:, 0], x_val[:, 1], s=3)
plt.savefig('val_lp_long.png')
