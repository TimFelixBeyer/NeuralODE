plt.scatter(x_train[:, 0], x_train[:, 1], alpha=0.1, s=3)
plt.savefig('train_long_period_long.png')
plt.close()
plt.scatter(x_train[:, 2], x_train[:, 3], alpha=0.1, s=3)
plt.savefig('train_short_period_long.png')
plt.close()
plt.scatter(x_train[:, 4], x_train[:, 5], alpha=0.1, s=3)
plt.savefig('train_long_period_lat.png')
plt.close()
plt.scatter(x_train[:, 6], x_train[:, 7], alpha=0.1, s=3)
